struct Line{ 
	/*! Defining line data structure which represents an edges in the drawing/object */
	int x1,y1,z1,x2,y2,z2;
	float r,g,b;
	int state;
	int total;
};
struct Point3D{		
	/*! Data Structure to store the 3D points */
    int x1,y1,z1;
};
struct Point2D{
	/*! Data Structure to store 2D points */
    int x1,y1,x2,y2;
};
